package day8;

public class StringArray2 {

    public static void main(String[] args) {


        String[] names = new String[3];
        names[0] = "John";
        names[1] = "Mike";
        names[2] = "Jane";

        for (int i = 0; i < names.length; i++) {
            System.out.println(names[i]);
        }
    }
}